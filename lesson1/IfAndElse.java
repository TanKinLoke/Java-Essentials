package lesson1;

public class IfAndElse {

	public static void main(String[] args) {
		/*
		 == is equal to
		 != is not equal to
		 > is greater than
		 < is less than
		 >= is greater than or equal to
		 <= is less than or equal to
		 */
		int x = 10;
		
		if (x == 10) {
			System.out.println("yes x == 10");
		}
		else {
			System.out.println("no x != 10");
		}
	}

}
